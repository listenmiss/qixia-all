package cn.com.catcher.vision.gov.qixia.model;

public class SyncTrigger {

	 private Integer id;
	 private String sync_name;
	 private String sync_key;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getSync_name() {
		return sync_name;
	}
	public void setSync_name(String sync_name) {
		this.sync_name = sync_name;
	}
	public String getSync_key() {
		return sync_key;
	}
	public void setSync_key(String sync_key) {
		this.sync_key = sync_key;
	}
	 
	 
	 
	 
}
