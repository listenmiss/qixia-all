package cn.com.catcher.vision.gov.qixia.model;

import java.util.Date;

public class User {
    private int id;
    private String licenceNumber;
    private String nickname;
    private String userphone;
    private String userpassword;
    private String clientip;
    private int userstate;
    private String realid;
    private int usertype;
    private Date validdate;
    private String username;
    private int signstate;
    private String liepassword;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLicenceNumber() {
		return licenceNumber;
	}
	public void setLicenceNumber(String licenceNumber) {
		this.licenceNumber = licenceNumber;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUserphone() {
		return userphone;
	}
	public void setUserphone(String userphone) {
		this.userphone = userphone;
	}
	public String getUserpassword() {
		return userpassword;
	}
	public void setUserpassword(String userpassword) {
		this.userpassword = userpassword;
	}
	public String getClientip() {
		return clientip;
	}
	public void setClientip(String clientip) {
		this.clientip = clientip;
	}
	public int getUserstate() {
		return userstate;
	}
	public void setUserstate(int userstate) {
		this.userstate = userstate;
	}
	public String getRealid() {
		return realid;
	}
	public void setRealid(String realid) {
		this.realid = realid;
	}
	public int getUsertype() {
		return usertype;
	}
	public void setUsertype(int usertype) {
		this.usertype = usertype;
	}
	public Date getValiddate() {
		return validdate;
	}
	public void setValiddate(Date validdate) {
		this.validdate = validdate;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getSignstate() {
		return signstate;
	}
	public void setSignstate(int signstate) {
		this.signstate = signstate;
	}
	public String getLiepassword() {
		return liepassword;
	}
	public void setLiepassword(String liepassword) {
		this.liepassword = liepassword;
	}

	

}